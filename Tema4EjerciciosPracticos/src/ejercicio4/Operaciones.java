package ejercicio4;

public class Operaciones {
	
	double numero1, numero2;
	
	
	double sumar () {
		double suma;
		suma=numero1+numero2;
		return (suma);
	}
	
	double restar () {
		double resta;
		resta=numero1-numero2;
		return (resta);
	}
	
	double multiplicar () {
		double multiplicacion;
		multiplicacion=numero1*numero2;
		return (multiplicacion);
	}
	
	double dividir () {
		double division;
		division=numero1/numero2;
		return (division);
	}

}
